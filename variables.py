# 홍길동씨 개인정보  
name = "홍길동"
age = 25
height = 184.6
print(type(name)) # 이름의 변수는 srting 문자열
print(type(age)) # 나이의 변수는 intiger 변수
print(type(height)) # 키의 변수는 float 실수,유리수

# 변수의 이름 짓는것에 대한 규칙

# 1. 영문자(대,소문자 구분) 숫자 언더바 한글 사용가능
# ex) AGE, Age , age , age3 다 다르다.

# 2. 첫 자리에는 숫자를 사용할 수 없다.

# 3. 파이썬에서 자체적으로 사용하고 있는 단어는 사용할수 없다.
# ex) if, true while...


# Code convention 협업을 할때 사람들과 이름을 짓는 약속
# 1. 한 단어로 된 변수는 소문자로 적는다. age, good, name...

# 2. 두 단어 이상의 변수는 언더바로 구분 my_name, my_friend...

# 3. 예약어와 충돌하는 경우에는 뒤에 언더바를 붙인다. 
# if_, time_ ...

# 4. 상수의 경우 대문자와 언더바를 혼용하여 쓴다.
# TOTAL, MAX_NUM, AVG....
